// class definition
public class Sumofnumbers {
  // main method
  public static void main(String[] args) {
    // declare and initialize variables
    int x = 6;
    int y = 9;
    // calculate the sum
    int sum = x + y;
    // display the sum
    System.out.println("The sum of " + x + " and " + y + " is " + sum);
  }
}
